function updateResults() {
    const letter = document.getElementById("input-letter").value.trim();
    const results = data[letter] || ["", "", "", "", ""];

    document.getElementById("human-name").textContent = results[0];
    document.getElementById("animal").textContent = results[1];
    document.getElementById("plant").textContent = results[2];
    document.getElementById("object").textContent = results[3];
    document.getElementById("country").textContent = results[4];
}

function copyToClipboard(elementId) {
    const text = document.getElementById(elementId).textContent;
    if (text) {
        navigator.clipboard.writeText(text).then(() => {
            alert("Copied to clipboard: " + text);
        }).catch(err => {
            console.error("Could not copy text: ", err);
        });
    }
}
